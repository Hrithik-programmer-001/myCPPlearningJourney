#include <iostream>
using namespace std;

int main(){
  
    int num1 = 15; //Decimal
    int num2 = 0456; //Octal (0)
    int num3 = 0x09af; //Hexadecimal (0x)
    int num4 = 0b011011011; //Binary (0b)

    cout << "Number 1 :- "<< num1 <<endl;
    cout << "Number 2 :- "<< num2 <<endl;
    cout << "Number 3 :- "<< num3 <<endl;
    cout << "Number 4 :- "<< num4 <<endl;

    return 0;

}